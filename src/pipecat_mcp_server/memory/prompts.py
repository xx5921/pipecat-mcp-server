"""记忆模块用到的 LLM 提示词。

包含两个 prompt：
- ``SUMMARIZE_PROMPT``：将旧对话累积压缩成一段叙述体摘要。
- ``FACT_EXTRACTION_PROMPT``：从最近对话中提取用户画像 KV。
"""

# 对话摘要 prompt：压缩合并
# 核心策略：把旧摘要 + 新对话合并成一个**精简**的新摘要。
# 只记用户的「关键信息」（事实、事件、偏好、计划），不记冰糖的反应/态度/评价。
# 如果旧摘要太长，允许精简旧内容的表述来腾出空间。
SUMMARIZE_PROMPT = """你的任务是把「已有摘要」和「新对话」合并成一段精简的中文摘要。

【已有摘要】：
{existing_summary}

【新加入的对话】：
{new_messages}

请输出合并后的摘要。要求：

1. **只记关键信息**：用户的事实（姓名、职业、家庭等）、事件（做了什么、计划什么）、偏好（喜欢什么、讨厌什么）、情绪状态。这些必须保留。
2. **不记废话**：不要写冰糖的反应、态度、评价（如"冰糖表示关心""展现了亲和力"这类全部删掉）。
3. **允许精简旧摘要**：如果旧摘要太长或表述啰嗦，可以精简文字，只要核心信息不丢就行。
4. **总字数控制在 300-500 字**。如果信息多，优先保留事实和事件，砍掉形容词和客套话。
5. 用简洁的叙述体写，以「用户」为主语。
6. 保留时间感（「最近」「之前」「计划明天」等）。

直接输出摘要，不要任何前缀或说明。
"""


# 事实提取 prompt：从对话中提取用户画像 KV
# 参考 Mem0 的 USER_MEMORY_EXTRACTION_PROMPT，但简化为固定字段 KV
FACT_EXTRACTION_PROMPT = """你的任务是从一段中文对话中，提取关于用户的「关键事实」。

已有事实：
{existing_facts}

最近的对话：
{recent_messages}

请提取或更新以下类型的用户事实（如果对话中确实出现了的话）：
- name: 用户的称呼
- job: 职业或学业
- hobby: 兴趣爱好
- family: 家庭情况
- location: 所在地
- food: 饮食偏好
- mood: 最近情绪状态
- recent_events: 最近提到的具体事件
- other: 其他值得长期记住的事

要求：
- 只提取对话中明确出现的事实，不要瞎编或推测
- 与已有事实冲突时，以新对话为准（更新为最新值）
- 输出 JSON 格式：{{"key": "value", ...}}
- 如果对话中没有新的可提取事实，输出：{{}}

直接输出 JSON，不要 markdown 代码块，不要前后说明。
"""
